public class StudySession {

    int id;
    String subject;
    String topic;
    int duration;
    String date;

    public StudySession(int id, String subject, String topic, int duration, String date) {
        this.id = id;
        this.subject = subject;
        this.topic = topic;
        this.duration = duration;
        this.date = date;
    }
}